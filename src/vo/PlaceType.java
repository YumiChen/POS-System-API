package vo;

public class PlaceType {
	int id;
	String name;
	int start;
	int rowsperpage;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getStart() {
		return start;
	}
	public void setStart(int start) {
		this.start = start;
	}
	public int getRowsperpage() {
		return rowsperpage;
	}
	public void setRowsperpage(int rowsperpage) {
		this.rowsperpage = rowsperpage;
	}
	@Override
	public String toString() {
		return "PlaceType [id=" + id + ", name=" + name + ", start=" + start + ", rowsperpage=" + rowsperpage + "]";
	}

}
